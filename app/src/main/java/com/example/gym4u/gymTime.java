package com.example.gym4u;

public class gymTime {
    int mon,tue,wed,thu,fri,sat,sun;
    public gymTime(){

    }



    public void setMon(int mon) {
        this.mon = mon;
    }

    public void setTue(int tue) {
        this.tue = tue;
    }

    public void setWed(int wed) {
        this.wed = wed;
    }

    public void setThu(int thu) {
        this.thu = thu;
    }

    public void setFri(int fri) {
        this.fri = fri;
    }

    public void setSat(int sat) {
        this.sat = sat;
    }

    public void setSun(int sun) {
        this.sun = sun;
    }

    public int getMon() {
        return mon;
    }

    public int getTue() {
        return tue;
    }

    public int getWed() {
        return wed;
    }

    public int getThu() {
        return thu;
    }

    public int getFri() {
        return fri;
    }

    public int getSat() {
        return sat;
    }

    public int getSun() {
        return sun;
    }
}
